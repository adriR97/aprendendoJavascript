//atribuindo valor às variáveis
const a = 7
let b = 3

console.log("------------------Soma-----------------")
console.log(b += a)                                                    //b = b + a
console.log("\n---------------Subtração---------------")
console.log(b -= 4)                                                    //b = b - 4
console.log("\n-------------Multiplicação-------------")
console.log(b *= 2)                                                    //b = b * 2
console.log("\n----------------Divisão----------------")
console.log(b /= 2)                                                    //b = b / 2
console.log("\n-------------Módulo(resto)-------------")
console.log(b %= 2)                                                    //b = b % 2